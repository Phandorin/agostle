// readKeyring reads the keyring, and panics on error
func readKeyring(r io.Reader) openpgp.KeyRing {
	var keyring openpgp.EntityList
	for {
		el, err := openpgp.ReadArmoredKeyRing(r)
		if err != nil {
			if len(keyring) == 0 {
				panic(err)
			}
			break
		}
		keyring = append(keyring, el...)
	}
    return keyring
}

